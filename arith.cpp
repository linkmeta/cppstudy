#include <iostream>

using namespace std;

int main(void){
    int hats;
    int heads;
    cout << "Please enter the hats num:" << endl;
    cin >> hats;
    cout << "Please enter the heads num:" << endl;
    cin >> heads;

    cout << "hats + heads = " << hats + heads << endl;
    cout << "hats - heads = " << hats - heads << endl;
    cout << "hats / heads = " << hats/heads << endl;
    cout << "hats % heads = " << hats%heads << endl;
    return 1;

}